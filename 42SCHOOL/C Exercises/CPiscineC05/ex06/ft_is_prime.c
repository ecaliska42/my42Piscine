int	ft_is_prime(int nb)
{
	if(nb % nb == 0 && nb > 1)
		return(1);
	return(0);
}

#include <stdio.h>
int	main(void)
{
int	nb;
nb = 9;
printf("%d", ft_is_prime(nb));
}
